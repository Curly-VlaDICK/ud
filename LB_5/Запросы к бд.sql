--Составить список (Ф.И.О.) заказчиков из Азербайджана.
SELECT CONCAT(фамилия, ' ', имя, ' ', отчество) AS ФИО
FROM Customer
WHERE страна = 'Азербайджан';
--Выбрать список предприятий, на которые еще не установлен телефон.
SELECT предприятие
FROM Customer
WHERE телефон IS NULL OR телефон = '';
--айти код покупателей, которым сегодня продан товар кода ... (например, код товара 1).
SELECT код_покупателя
FROM Ord_Sale
WHERE код_товар = 1 AND дата_продажи = CURDATE();
--Выбрать предприятие, не бравшее кредита.
SELECT предприятие
FROM Customer
WHERE кредит = 0;
--Найти Ф.И.О. заказчика, проживающего по адресу: Пронокская,10/243.
SELECT CONCAT(фамилия, ' ', имя, ' ', отчество) AS ФИО
FROM Customer
WHERE адрес = 'Пронокская,10/243';
--Найти Ф.И.О. заказчиков, у которых день рождения 9.0?.?5г. (например, 9.05.1995).
SELECT CONCAT(фамилия, ' ', имя, ' ', отчество) AS ФИО
FROM Customer
WHERE DATE_FORMAT(дата_рождения, '%d.%m.%Y') LIKE '09.05.1995';
--По предприятию К..з найти телефон.
SELECT телефон
FROM Customer
WHERE предприятие LIKE 'К%з';
--Список предприятий, для которых скидка > 15%.
SELECT название
FROM Postav
WHERE скидки > 15;
--Найти адрес заказчиков из Одессы с кредитом 50000.
SELECT адрес
FROM Customer
WHERE город = 'Одесса' AND кредит = 50000;
--Найти предприятия, чей кредит 100000 или имеют скидку 30%.
SELECT предприятие
FROM Customer
WHERE кредит = 100000 OR код_поставщика IN (SELECT код_поставщика FROM Postav WHERE скидки = 30);
--Посчитать количество заказчиков, у которых день рождения в мае.
SELECT COUNT(*) AS Количество
FROM Customer
WHERE MONTH(дата_рождения) = 5;
--Найти дату, когда было принято max количество заказов.
SELECT дата_заказа
FROM Ord_Sale
GROUP BY дата_заказа
ORDER BY COUNT(*) DESC
LIMIT 1;
--Найти день, в который было принято min количество заказов.
SELECT дата_заказа
FROM Ord_Sale
GROUP BY дата_заказа
ORDER BY COUNT(*) ASC
LIMIT 1;
--Найти средний кредит для предприятий из Московской области.
SELECT AVG(кредит) AS СреднийКредит
FROM Customer
WHERE область = 'Москва';
--Посчитать сумму кредитов заказчиков из Уфы.
SELECT SUM(кредит) AS СуммаКредитов
FROM Customer
WHERE город = 'Уфа';
--Сколько товаров разных наименований на складе.
SELECT COUNT(DISTINCT наименования_продукта) AS КоличествоНаименований
FROM Goods;
--Объединить данные полей: код заказчика и фамилия.
SELECT CONCAT(код_покупателя, ' ', фамилия) AS КодФамилия
FROM Customer;
--Найти телефон заказчиков, заказавших глушитель.
SELECT C.телефон
FROM Customer C
JOIN Ord_Sale O ON C.код_покупателя = O.код_покупателя
WHERE O.код_товар = 1; -- Код товара для глушителя
--Если предприятие изменило номер телефона, то изменить номер в таблице.
UPDATE Customer
SET телефон = '9876543210'
WHERE предприятие = 'Предприятие А' AND телефон = '1234567890';
--Удалить из таблицы записи предприятий, не бравших кредит.
DELETE FROM Customer
WHERE кредит = 0;